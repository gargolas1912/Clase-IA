def pipeline_completo(df):

    print("=" * 50)
    print("INICIANDO PIPELINE MULTIAGENTE")
    print("=" * 50)

    # --- AGENTE 1: Normalización ---
    print("\n[AGENTE 1] Normalizando datos...")

    agente1 = AgenteNormalizador()

    X_train, X_test, y_train, y_test = agente1.procesar(df)

    print(f"  Train: {X_train.shape} | Test: {X_test.shape}")

    # --- AGENTE 2: Entrenamiento ---
    print("\n[AGENTE 2] Entrenando modelos...")

    agente2 = AgenteEntrenador()

    metricas, mejor_modelo = agente2.entrenar(
        X_train,
        X_test,
        y_train,
        y_test
    )

    print(f"\n  Mejor modelo seleccionado: {mejor_modelo}")

    for nombre, vals in metricas.items():
        print(f"\n  {nombre}:")
        for metrica, valor in vals.items():
            print(f"    {metrica}: {valor:.4f}")

    # --- AGENTE 3: Comunicación ---
    print("\n[AGENTE 3] Generando reporte con Transformer...")

    agente3 = AgenteComunicador(
        tokenizer,
        modelo,
        df=df,
        metricas=metricas,
        mejor_modelo=mejor_modelo
    )

    reporte = agente3.generar_reporte(
        metricas,
        mejor_modelo
    )

    print("\n" + "=" * 50)
    print("REPORTE FINAL")
    print("=" * 50)
    print(reporte)

    # Chat interactivo opcional
    agente3.chat_interactivo()

    return {
        "metricas": metricas,
        "mejor_modelo": mejor_modelo,
        "reporte": reporte
    }


# Ejecutar pipeline completo
