import torch

class AgenteComunicador:

    def __init__(self, tokenizer, modelo, df=None, metricas=None, mejor_modelo=None):
        self.tokenizer = tokenizer
        self.modelo = modelo
        self.contexto_dataset = ""
        self.contexto_metricas = ""

        if df is not None and metricas is not None and mejor_modelo is not None:
            self._construir_contexto(df, metricas, mejor_modelo)

    def _construir_contexto(self, df, metricas, mejor_modelo):

        datos_mejor = metricas[mejor_modelo]

        churn_counts = df['Churn'].value_counts().to_dict()

        self.contexto_dataset = (
            f"This dataset has {df.shape[0]} rows and {df.shape[1]} columns. "
            f"Customers who churned (Yes): {churn_counts.get('Yes', 0)}. "
            f"Customers who stayed (No): {churn_counts.get('No', 0)}. "
            f"Columns: {', '.join(df.columns.tolist())}."
        )

        self.contexto_metricas = (
            f"The best model is {mejor_modelo} with "
            f"accuracy {datos_mejor['accuracy']:.3f}, "
            f"precision {datos_mejor['precision']:.3f}, "
            f"recall {datos_mejor['recall']:.3f}, "
            f"and F1 score {datos_mejor['f1']:.3f}."
        )

    def _generar(self, prompt, max_new_tokens=200, num_beams=4):

        inputs = self.tokenizer(
            prompt,
            return_tensors="pt",
            truncation=True,
            max_length=512
        )

        with torch.no_grad():
            outputs = self.modelo.generate(
                **inputs,
                max_new_tokens=max_new_tokens,
                num_beams=num_beams,
                early_stopping=(num_beams > 1)
            )

        return self.tokenizer.decode(
            outputs[0],
            skip_special_tokens=True
        )

    def generar_reporte(self, metricas, mejor_modelo):

        datos = metricas[mejor_modelo]

        prompt = f"""
        Generate a brief academic report in Spanish.

        Selected model: {mejor_modelo}
        Accuracy: {datos['accuracy']:.3f}
        Precision: {datos['precision']:.3f}
        Recall: {datos['recall']:.3f}
        F1 Score: {datos['f1']:.3f}

        Explain the results in natural language.
        """

        return self._generar(prompt, max_new_tokens=200)

    def responder_pregunta(self, pregunta):

        pregunta_lower = pregunta.lower()

        if any(k in pregunta_lower for k in ["fila", "columna", "dataset", "churn", "abandon", "cliente"]):
            contexto_usar = self.contexto_dataset
        elif any(k in pregunta_lower for k in ["modelo", "accuracy", "precision", "recall", "f1", "metrica", "métrica"]):
            contexto_usar = self.contexto_metricas
        else:
            contexto_usar = self.contexto_dataset + " " + self.contexto_metricas

        prompt = f"""Answer the question using only the information below.

Information:
{contexto_usar}

Question: {pregunta}
Answer:"""

        return self._generar(prompt, max_new_tokens=80, num_beams=1)

    def chat_interactivo(self):

        print("=" * 50)
        print("CHAT INTERACTIVO - PREGUNTAS SOBRE EL CONTEXTO")
        print("Escribí 'salir' para terminar")
        print("=" * 50)

        while True:

            pregunta = input("\nTu pregunta: ")

            if pregunta.lower().strip() == "salir":
                print("Chat finalizado.")
                break

            respuesta = self.responder_pregunta(pregunta)

            print("Respuesta:", respuesta)

