from sklearn.ensemble import RandomForestClassifier
from sklearn.linear_model import LogisticRegression

from sklearn.metrics import accuracy_score
from sklearn.metrics import precision_score
from sklearn.metrics import recall_score
from sklearn.metrics import f1_score

class AgenteEntrenador:

    def entrenar(
        self,
        X_train,
        X_test,
        y_train,
        y_test
    ):

        modelos = {

            "Random Forest":
                RandomForestClassifier(
                    n_estimators=200,
                    random_state=42
                ),

            "Logistic Regression":
                LogisticRegression(
                    max_iter=300
                )
        }

        mejor_modelo = None
        mejor_f1 = 0

        resultados = {}

        for nombre, modelo in modelos.items():

            print(f"\nEntrenando {nombre}")

            modelo.fit(
                X_train,
                y_train
            )

            pred = modelo.predict(
                X_test
            )

            accuracy = accuracy_score(
                y_test,
                pred
            )

            precision = precision_score(
                y_test,
                pred
            )

            recall = recall_score(
                y_test,
                pred
            )

            f1 = f1_score(
                y_test,
                pred
            )

            resultados[nombre] = {

                "accuracy": accuracy,
                "precision": precision,
                "recall": recall,
                "f1": f1
            }

            if f1 > mejor_f1:

                mejor_f1 = f1
                mejor_modelo = nombre

        return resultados, mejor_modelo

